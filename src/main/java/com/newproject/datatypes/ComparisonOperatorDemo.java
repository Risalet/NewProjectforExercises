package com.newproject.datatypes;

public class ComparisonOperatorDemo {
    public static void main(String[] args) {
        int i=20;
        int b=24;
        System.out.println(i==b);//false
        System.out.println(i!=b);//true
        System.out.println(i>=b);//false
        System.out.println(i<=b);//true
        System.out.println(i>b);//false
        System.out.println(i<b);//true
    }
}
