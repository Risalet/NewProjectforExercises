package com.newproject.datatypes;

public class ArithmeticOperatorsDemo1 {
    public static void main(String[] args) {
        int x=100;
        x++;
        System.out.println(x);

        int x1=100;
        x1+=100;//x1=x1-100
        System.out.println(x1);

        x1-=50;//x1-50
        System.out.println(x1);

        x+=20;//x+20=120;
        System.out.println(x);

        x-=20;//x-20=80;
        System.out.println(x);

        x*=2;//x*2=200;
        System.out.println(x);

        x/=25;//x/25=4;
        System.out.println(x);

        x%=3;//x/3=kalduk;
        System.out.println(x);
    }
}
